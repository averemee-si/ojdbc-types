	private static class MapHolder {

		private final Map<String, Integer> nameToId;
		private final Map<Integer, String> idToName;
		private final Map<Integer, String> oldIdToName;

		MapHolder() {
			nameToId = new HashMap<>();
			idToName = new HashMap<>();
			oldIdToName = new HashMap<>();
		}
		
		void put(final String name, final int id) {
			nameToId.put(name,  id);
			idToName.put(id, name);
		}

		void putOld(final String name, final int id) {
			oldIdToName.put(id, name);
		}

		int getId(final String name) {
			return nameToId.get(name);
		}

		String getName(final int id) {
			return idToName.get(id);
		}

		String getOldName(final int id) {
			return oldIdToName.get(id);
		}

	}

